import matplotlib.pylab as pl
import numpy as np
X = np.arange(0.01, 5, 0.001)
V0 = 0.0277/(X * X)
pl.hold(True)
V1 = 0.0044/(X * X) 
V2 = 0.0043/(X * X) 
V3 = 0.0047/(X * X) 
V4 = 0.0037/(X * X) 
V5 = 0.005/(X * X) 
pl.plot (X, V0, "m")
pl.plot (X, V1, "b")
pl.plot (X, V2, "y")
pl.plot (X, V3, "g")
pl.plot (X, V4, "k")
pl.plot (X, V5, "r")
pl.xlabel("Distancia [m]")
pl.ylabel("Voltaje [V]")
pl.title("Distancia vs Voltaje")
pl.axis([0.05, 0.6, 0.005, 0.5])
pl.legend( ('V0 = 0.0277 X^(-2)', 'V1 = 0.0044 X^(-2)', 'V2 = 0.0043 X^(-2)', 'V3 = 0.0047 X^(-2)', 'V4 = 0.0037 X^(-2)', 'V5 = 0.005 X^(-2)'), loc = 'upper right')
pl.savefig('img/reflectancia.png')
pl.show()
